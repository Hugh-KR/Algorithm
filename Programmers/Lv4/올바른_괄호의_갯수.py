"""
괄호 쌍의 갯수에 따른 경우의 수
1 - () 1
2 - (()), ()() 2
3 - ((())), (())(), ()(()), (()()), ()()() 5
4 - (((()))), ((())) (), () ((())), ((()) ()), (()) (()),
    (()) () (), () (()) (), () () (()), (() (())), ((() ())), 
    (() () ()),(() ()) (), () (() ()), () () () () 14

"""
def solution(n):
    dp = [0] * 15
    dp[0] = 1
    dp[1] = 1
    dp[2] = 2
    for i in range(3, len(dp)):
        for j in range(0, i):
            dp[i] += (dp[j]*dp[i-(j+1)])
    return dp[n]